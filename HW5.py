# Доброго времени суток: программа принимает на вход текущее время с помощью input
# и возвращает приветствие: “Доброе утро!”, “Добрый день!”, “Добрый вечер!”, “Доброй ночи!”
time = float(input("Введите время через точку: "))
if 6.00 <= time <= 12.00:
    print("Доброе утро!")
elif 12.00 < time <= 18.00:
    print("Добрый день!")
elif 18.00 < time <= 22.00:
    print("Добрый вечер!")
elif 22.00 < time <= 23.59 or 0.00 <= time <= 5.59:
    print("Доброй ночи!")

# Угадай число
# Напишите программу, которая помогает вам угадать сгенерированное число.
# В цикле while программа принимает на вход ваше число и сообщает:
# число больше сгенерированного или меньше сгенерированного.
# Цикл заканчивает работу, если вы угадали число. Программа выводит на экран число.
# Усложните программу: добавьте счетчик ваших попыток угадать число.

import random
target_number = int(random.randint(1, 100))
schetchik = 0

while True:
    your_number = int(input("Угадай число от 1 до 100: "))
    schetchik += 1

    if your_number < target_number:
        print("Это число меньше сгенерированного")
    elif your_number > target_number:
        print("Это число больше сгенерированного")
    else:
        print(f"Браво! Ты угадал число {target_number}!")
        break

print(f"Ты пытался угадать {schetchik} раз(а)")