# Конвертер оценок (из 10-балльной в 5-балльную)
grade = float(input("Оценка по 10-балльной шкале: "))
from math import *
grade = ceil(grade)
if 0 < grade <= 3:
    print(2, "Неудовлетворительно", sep = "\n")
elif 4 <= grade <= 5:
    print(3, "Удовлетворительно", sep = "\n")
elif 6 <= grade <= 7:
    print(4, "Хорошо", sep = "\n")
elif 8 <= grade <= 10:
    print(5, "Отлично", sep = "\n")
else:
    print("Вы точно ввели оценку?")

